package test_01;

public class que_04_type_casting {

    public static void main(String[] args) {


        byte a = 10;

        int c = a;    // implicit typecasting

        System.out.println(c);


        int d = 12;

        short e = (short)d;   // explicit typecasting

        System.out.println(e);

    }
}
